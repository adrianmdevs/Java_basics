public class logicalOperator1 {
    public static void main(String[] args) {
        int x = 67;
        int y = 46;
        System.out.println(x<100||x>50);
        System.out.println(!(y<50&&y<100));

    }
    // && And >> True if both statements are true else false
    // || OR >> True if one of the statements is true
    // ! NOT >> Reverses the result false = true and true = false
}
